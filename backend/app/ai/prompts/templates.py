def build_dynamic_context(cart: list, user_id: int, summary: str = "", user_profile: str = "") -> str:
    """构建动态上下文信息"""
    parts = []

    if cart:
        total = sum(c["unit_price"] * c["quantity"] for c in cart)
        lines = [f"  {c['name']} x{c['quantity']} = {c['unit_price'] * c['quantity']:.0f}元" for c in cart]
        parts.append(f"## 当前购物车（合计 {total:.0f} 元）\n" + "\n".join(lines))
    else:
        parts.append("## 当前购物车\n购物车为空")

    if user_profile:
        parts.append(user_profile)

    if summary:
        parts.append(f"## 对话摘要\n{summary}")

    return "\n\n".join(parts)
